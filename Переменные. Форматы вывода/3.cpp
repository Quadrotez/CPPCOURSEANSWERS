#include<iostream>          
using namespace std;          
main()          
{          
	 int a=10, b=5;                
	 cout<<a<<"+"<<b<<"="<<a+b<<endl;  // 10+5=15         
	 cout<<a<<"-"<<b<<"="<<a-b<<endl;  // 10+5=15        
	 cout<<a<<"*"<<b<<"="<<a*b<<endl;  // 10+5=15        
	 cout<<a<<"/"<<b<<"="<<a/b<<endl;  // 10+5=15        
}